/*:
## Creating Variables in Swift
**To create a variable, use the following syntax:**

*var nameOfVariable = value*
*/
var myJerseyNumber = 5

var myCoolVariable: Character = "a"

/*:
**To create a variable and declare its explicit type, use the following syntax:**

*var nameOfVariable: Type = value*
*/
var numberOfWheels : Int = 4
var inchesOfRainfall: Float = 3.5
var attackRating: Double = 4210.50
var letterGuessed: Character = "e"
var secretWord: String = "Barnacles"
var rainingOutside: Bool = true

/*:
**To change the value of a variable, use the following syntax:**

*nameOfVariable = newValue*
*/
myJerseyNumber = 80
myJerseyNumber = 16
myJerseyNumber = 12
myJerseyNumber = 84

// myJerseyNumber = "I Like The Number 80"

